# ── BOUNDS GUARDS ─────────────────────────────────────────────────────────────

def _safe_profile(domain: str, asset: str) -> Tuple[str, Dict[str, Any]]:
    """
    Resolve domain safely, look up profile, clamp severity and onset_frac.
    Returns (resolved_domain, clamped_profile).
    resolved_domain may differ from domain if domain was unknown.
    """
    resolved_domain = domain if domain in DEMO_PROFILES else "seismic"
    profile = DEMO_PROFILES[resolved_domain].get(asset, _FALLBACK_PROFILE)
    return resolved_domain, {
        **profile,
        "severity":   float(np.clip(profile.get("severity",   0.6), _MIN_SEVERITY, _MAX_SEVERITY)),
        "onset_frac": float(np.clip(profile.get("onset_frac", 0.3), _MIN_ONSET,    _MAX_ONSET)),
    }


def _normalize(sig: np.ndarray) -> WaveformArray:
    """Normalize signal to [-1, 1] — required by engine input guard."""
    peak = float(np.max(np.abs(sig))) + 1e-9
    return (sig / peak).astype(float)


# ── PUBLIC API ────────────────────────────────────────────────────────────────

def get_assets(domain: str) -> List[str]:
    """Return list of known demo asset keys for a domain. Useful for UI dropdowns."""
    resolved = domain if domain in DEMO_PROFILES else "seismic"
    return list(DEMO_PROFILES[resolved].keys())


def get_demo_tape(domain: str, asset: str) -> Tuple[WaveformArray, DemoMetadata]:
    """
    Generate a full synthetic precursor tape for a given domain/asset.
    Returns (waveform, metadata).
    Metadata is safe to include in API responses — no engine internals exposed.
    """
    resolved_domain, profile = _safe_profile(domain, asset)

    # Safe archetype lookup — falls back to fault_cascade if archetype unknown
    fn = _ARCHETYPES.get(
        profile["archetype"],
        _ARCHETYPES[_FALLBACK_PROFILE["archetype"]],
    )

    raw = fn(
        n          = _N_SAMPLES,
        severity   = profile["severity"],
        seed       = profile["seed"],
        onset_frac = profile["onset_frac"],
    )

    meta: DemoMetadata = {
        "profile_id":        f"{resolved_domain}:{asset}:{profile['archetype']}",
        "archetype":         profile["archetype"],
        "synthetic":         True,
        "asset":             asset,
        "domain":            resolved_domain,
        "requested_domain":  domain,
        "severity":          profile["severity"],
        "onset_frac":        profile["onset_frac"],
        "disclaimer":        PUBLIC_DEMO_DISCLAIMER,
    }
    return _normalize(raw), meta


def get_window(domain: str, asset: str,
               scan_index: int) -> Tuple[WaveformArray, DemoMetadata]:
    """
    Return the windowed slice for a given scan count, looping the tape.
    Returns (window, metadata).
    """
    scan_index          = max(0, int(scan_index))
    tape, meta          = get_demo_tape(domain, asset)
    total_windows       = (len(tape) - WINDOW_SIZE) // _STEP + 1
    idx                 = scan_index % max(total_windows, 1)
    start               = idx * _STEP
    meta["scan_index"]    = scan_index
    meta["window_index"]  = idx
    meta["total_windows"] = total_windows
    return tape[start:start + WINDOW_SIZE], meta
