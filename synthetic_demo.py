# ── COLORED NOISE ─────────────────────────────────────────────────────────────

def _pink_noise(n: int, rng: np.random.Generator) -> np.ndarray:
    """1/f (pink) noise — more realistic background than white noise."""
    white    = rng.standard_normal(n)
    fft      = np.fft.rfft(white)
    freqs    = np.fft.rfftfreq(n)
    freqs[0] = 1.0
    fft     /= np.sqrt(freqs)
    sig      = np.fft.irfft(fft, n=n)
    return sig / (np.std(sig) + 1e-9)


# ── PRECURSOR ARCHETYPES ──────────────────────────────────────────────────────
# All functions share the same signature:
#   (n, severity, seed, onset_frac) -> np.ndarray
# onset_frac is wired from DEMO_PROFILES — never hardcoded inside functions.

def _slow_fatigue(n: int, severity: float, seed: int,
                  onset_frac: float = 0.2) -> np.ndarray:
    rng   = np.random.default_rng(seed)
    t     = np.linspace(0, 4 * np.pi, n)
    sig   = _pink_noise(n, rng) * 0.08
    onset = int(n * onset_frac)
    ramp  = np.zeros(n)
    ramp[onset:] = np.power(np.linspace(0, 1, n - onset), 1.5) * severity
    sig  += ramp * np.sin(1.2 * t + rng.uniform(0, np.pi))
    n_spikes   = int(severity * 18)
    spike_locs = rng.integers(onset, n, size=n_spikes)
    for loc in spike_locs:
        width = rng.integers(2, 6)
        amp   = rng.uniform(0.3, severity * 1.2)
        sig[loc:min(loc + width, n)] += amp * rng.choice([-1, 1])
    return sig


def _bearing_fault(n: int, severity: float, seed: int,
                   onset_frac: float = 0.15) -> np.ndarray:
    rng       = np.random.default_rng(seed)
    t         = np.linspace(0, 8 * np.pi, n)
    sig       = _pink_noise(n, rng) * 0.06
    onset     = int(n * onset_frac)
    f_carrier = 0.8 + rng.uniform(-0.1, 0.1)
    ramp      = np.zeros(n)
    ramp[onset:] = np.linspace(0, severity, n - onset)
    for h in range(1, 5):
        amp  = severity / (h * 1.4)
        sig += ramp * amp * np.sin(h * f_carrier * t + rng.uniform(0, 2 * np.pi))
    f_fault = 0.15 + rng.uniform(-0.02, 0.02)
    mod     = 1 + ramp * 0.6 * np.sin(f_fault * t)
    sig    *= mod
    return sig


def _fracture_growth(n: int, severity: float, seed: int,
                     onset_frac: float = 0.25) -> np.ndarray:
    rng        = np.random.default_rng(seed)
    sig        = _pink_noise(n, rng) * 0.05
    onset      = int(n * onset_frac)
    freq_drift = np.linspace(0.4, 0.4 + severity * 0.6, n)
    phase      = np.cumsum(freq_drift) / n * 2 * np.pi
    ramp       = np.zeros(n)
    ramp[onset:] = np.linspace(0, severity * 0.7, n - onset)
    sig       += ramp * np.sin(phase)
    n_bursts    = int(3 + severity * 12)
    burst_times = np.sort(rng.integers(onset, n, size=n_bursts))
    for bt in burst_times:
        dur   = rng.integers(3, 10)
        amp   = rng.uniform(0.2, severity * 1.5)
        decay = np.exp(-np.linspace(0, 4, dur))
        end   = min(bt + dur, n)
        sig[bt:end] += amp * decay[:end - bt] * rng.choice([-1, 1])
    return sig


def _harmonic_lock(n: int, severity: float, seed: int,
                   onset_frac: float = 0.2) -> np.ndarray:
    rng     = np.random.default_rng(seed)
    t       = np.linspace(0, 10 * np.pi, n)
    sig     = _pink_noise(n, rng) * 0.07
    onset   = int(n * onset_frac)
    f_base  = 0.5
    f_shift = np.linspace(0, severity * 0.5, n)
    phase   = np.cumsum(f_base + f_shift) / n * 2 * np.pi
    ramp    = np.zeros(n)
    ramp[onset:] = np.linspace(0, severity, n - onset)
    sig    += ramp * np.sin(phase)
    for sb in [0.9, 1.1, 2.0]:
        sig += ramp * (severity * 0.3) * np.sin(
            sb * f_base * t + rng.uniform(0, np.pi))
    return sig


def _chaotic_transition(n: int, severity: float, seed: int,
                        onset_frac: float = 0.3) -> np.ndarray:
    rng   = np.random.default_rng(seed)
    onset = int(n * onset_frac)
    sig   = _pink_noise(n, rng) * 0.1
    r_chaos   = 3.6 + severity * 0.35
    x         = 0.5
    chaos_sig = np.zeros(n - onset)
    for i in range(len(chaos_sig)):
        x = r_chaos * x * (1 - x)
        chaos_sig[i] = x - 0.5
    blend      = np.linspace(0, severity, n - onset)
    sig[onset:] += blend * chaos_sig
    burst_start = int(n * max(onset_frac + 0.45, 0.82))
    if burst_start < n:
        sig[burst_start:] += rng.normal(0, severity * 0.8, n - burst_start)
    return sig


def _thermal_instability(n: int, severity: float, seed: int,
                         onset_frac: float = 0.1) -> np.ndarray:
    rng   = np.random.default_rng(seed)
    t     = np.linspace(0, 8 * np.pi, n)
    sig   = _pink_noise(n, rng) * 0.06
    onset = int(n * onset_frac)
    drift = np.zeros(n)
    drift[onset:] = np.sin(
        np.linspace(0, severity * np.pi, n - onset)) * severity * 0.4
    sig  += drift
    n_spikes  = int(4 + severity * 10)
    positions = np.linspace(onset, n - 10, n_spikes).astype(int)
    for i, pos in enumerate(positions):
        amp = severity * (0.2 + 0.8 * i / max(n_spikes, 1))
        sig[pos:min(pos + 4, n)] += amp * rng.choice([-1, 1])
    env_freq = 0.2 + rng.uniform(-0.05, 0.05)
    envelope = 1 + np.linspace(0, severity * 0.5, n) * np.abs(
        np.sin(env_freq * t))
    sig *= envelope
    return sig


def _fault_cascade(n: int, severity: float, seed: int,
                   onset_frac: float = 0.15) -> np.ndarray:
    rng        = np.random.default_rng(seed)
    sig        = _pink_noise(n, rng) * 0.05
    onset      = int(n * onset_frac)
    n_stages   = 3
    stage_size = (n - onset) // n_stages
    for stage in range(n_stages):
        start = onset + stage * stage_size
        end   = min(start + stage_size, n)
        amp   = severity * (0.3 + 0.35 * stage)
        half  = (end - start) // 2
        buildup = np.linspace(0, amp * 0.4, half)
        burst   = rng.normal(0, amp, end - start - half)
        sig[start:start + half] += buildup * np.sin(
            np.linspace(0, (stage + 1) * np.pi, half))
        sig[start + half:end] += burst
    return sig


def _resonance_shift(n: int, severity: float, seed: int,
                     onset_frac: float = 0.2) -> np.ndarray:
    rng     = np.random.default_rng(seed)
    sig     = _pink_noise(n, rng) * 0.06
    onset   = int(n * onset_frac)
    f_start = 0.3 + rng.uniform(-0.05, 0.05)
    f_end   = f_start + severity * 0.7
    freqs   = np.linspace(f_start, f_end, n)
    phase   = np.cumsum(freqs) / n * 2 * np.pi
    ramp    = np.zeros(n)
    ramp[onset:] = np.linspace(0, severity, n - onset)
    sig    += ramp * np.sin(phase)
    sig    += ramp * (severity * 0.25) * np.sin(
        2 * phase + rng.uniform(0, np.pi))
    return sig


